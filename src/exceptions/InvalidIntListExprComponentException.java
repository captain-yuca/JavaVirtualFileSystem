package exceptions;

public class InvalidIntListExprComponentException extends Exception {

	public InvalidIntListExprComponentException() {
	}

	public InvalidIntListExprComponentException(String arg0) {
		super(arg0);
	}

	public InvalidIntListExprComponentException(Throwable arg0) {
		super(arg0);
	}

	public InvalidIntListExprComponentException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
